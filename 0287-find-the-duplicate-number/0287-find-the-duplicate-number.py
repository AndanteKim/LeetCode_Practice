class Solution:
    def findDuplicate(self, nums: List[int]) -> int:
        duplicate, n = 0, len(nums) - 1
        bits = n.bit_length()
        
        for bit in range(bits):
            mask = 1 << bit
            base_count, nums_count = 0, 0
            for i in range(n + 1):
                # If bit is set in number (i) then add 1 to base_count
                if i & mask:
                    base_count += 1
                
                # If bit is set in number[i] then add 1 to nums_count
                if nums[i] & mask:
                    nums_count += 1
                
            # If the current bit is more frequently set in nums than it is in
            # the range [1, 2, ..., n] then it must also be set in the duplicate number
            if nums_count - base_count > 0:
                duplicate |= mask
        return duplicate
        
        